const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const promoCodeSchema = new Schema({
  code: {
    type: String,
    required: true,
    unique: true,
  },
  discount: {
    type: Number,
    required: true,
  },
  expiryDate: {
    type: Date,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const PromoCode = mongoose.model("PromoCode", promoCodeSchema);
module.exports = PromoCode;